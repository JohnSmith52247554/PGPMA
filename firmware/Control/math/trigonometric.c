float t_sin_degree(float degree)
{
    int32_t angle = (int32_t)(degree * 100) % 36000;
    if (angle < 0)
        angle += 36000;
    uint8_t quadrant = angle / 9000;
    switch (quadrant)
    {
    case 0:
        return sin_func[angle];
    case 1:
        return sin_func[18000 - angle];
    case 2:
        return -sin_func[angle - 18000];
    case 3:
        return -sin_func[36000 - angle];
    }
    return 0;
}

float t_cos_degree(float degree)
{
    return t_sin_degree(degree + 90.f);
}

float t_tan_degree(float degree)
{
    return t_sin_degree(degree) / t_cos_degree(degree);
}

float t_arcsin_degree(float x)
{
    if (x >= 0)
        return arcsin_func[(uint32_t)(x * 1000)];
    else
        return -arcsin_func[(uint32_t)(-x * 1000)];
}

float t_arccos_degree(float x)
{
    return t_arcsin_degree(-x) + 90;
}

float t_arctan_degree(float x)
{
    if (x < 0)
        return -t_arctan_degree(-x);
    else if (x <= 10)
        return arctan_func_0_10[(uint32_t)(x * 100)];
    else if (x <= 999)
        return arctan_func_10_1000[(uint32_t)x - 10];
    else
        return 90.f;
}

