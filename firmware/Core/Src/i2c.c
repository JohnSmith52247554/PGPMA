HAL_StatusTypeDef I2C3_Send_DMA(uint16_t addr, uint8_t *data, uint16_t len)
{
  unsigned long timeout = 0;
  while (i2c3_dma_state == I2C3_DMA_BUSY)
  {
    ++timeout;
    if (timeout >= 50000)
      return HAL_BUSY;
  }
  i2c3_dma_state = I2C3_DMA_BUSY;
  HAL_StatusTypeDef state = HAL_I2C_Master_Transmit_DMA(&hi2c3, addr, data, len);
  if (state != HAL_OK)
    i2c3_dma_state = I2C3_DMA_IDLE;
  return state;
}

